SELECT CONCAT(UPPER(SUBSTRING(FNAME,1,1)),LOWER(SUBSTRING(FNAME,2,14))) AS NAME, LEN(FNAME) FROM EMPLOYEE 
WHERE (FNAME LIKE 'J%' OR FNAME LIKE 'A%' OR FNAME LIKE 'M%');

SELECT E.SSN, D.DNAME FROM EMPLOYEE AS E, DEPARTMENT AS D WHERE E.DNO = D.DNUMBER;

SELECT FNAME, LNAME, SALARY FROM EMPLOYEE WHERE SALARY > 25000;

SELECT E.FNAME, E.LNAME, E.ADDRESS FROM EMPLOYEE AS E, DEPARTMENT AS D WHERE E.DNO = D.DNUMBER AND D.DNAME = 'Research';

