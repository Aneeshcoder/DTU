WHERE DNUM = DNUMBER AND MGR_SSN = SSN AND LNAME = 'Smith') 
UNION 
(SELECT DISTINCT PNUMBER FROM PROJECT, WORKS_ON, EMPLOYEE
WHERE PNUMBER = PNO AND ESSN = SSN AND LNAME = 'Smith');

