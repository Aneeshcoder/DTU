SELECT * FROM EMPLOYEE WHERE BDATE >= '1950-01-01' AND BDATE < '1960-01-01';

SELECT E.SALARY*1.1 FROM EMPLOYEE AS E, PROJECT AS P,WORKS_ON AS W WHERE P.PNAME = 'ProductX' AND W.PNO = P.PNUMBER AND W.ESSN = E.SSN;

